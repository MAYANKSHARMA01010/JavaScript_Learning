// = Assignment Operator(assign value to a variable)
// == Compare
// === Compare
let numberOfApples = 10;
let numberOfOranges = "10";
let numberOfMangoes = 20;

// == use to check the values not the type of data
let isEqual = numberOfApples == numberOfOranges;
console.log(isEqual);


// != use to check values are not equal or not
let isNotEqual = numberOfApples != 10;
console.log(isNotEqual);


// === use to check the value and also the type of data
let isStrictlyEqual = numberOfApples === numberOfOranges;
console.log(isStrictlyEqual);


// !== use to check type not data
let isStrictlyNotEqual = numberOfApples !== "10";
console.log(isStrictlyNotEqual);


// >
let isGreaterThan = numberOfApples > numberOfMangoes;
console.log(isGreaterThan);


// >
let isLessThan = numberOfApples < numberOfMangoes;
console.log(isLessThan);


//  >=
let isGreaterOrEqual = numberOfApples >= 10;
console.log(isGreaterOrEqual);


//  <=
let isLessOrEqual = numberOfApples <= 10;
console.log(isLessOrEqual);















